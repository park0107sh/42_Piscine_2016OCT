int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int long nc;

	nc = nb;
	if (nc < 0)
	{
		nc = (-1 * nc);
		ft_putchar('-');
	}
	if (nc >= 10)
	{
		ft_putnbr(nc / 10);
		ft_putchar((nc % 10) + 48);
	}
	else
	{
		ft_putchar(nc + 48);
	}
}
