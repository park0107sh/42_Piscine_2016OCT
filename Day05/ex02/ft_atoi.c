int		ft_atoi(char *str)
{
	int i;
	int neg;
	int num;

	i = 0;
	neg = 0;
	num = 0;
	while ((str[i] == ' ') || (str[i] == '\n') || (str[i] == '\t')
			|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		neg = 1;
	if ((str[i] == '+') || (str[i] == '-'))
		i++;
	while ((str[i] >= 48 && str[i] <= 57))
	{
		num *= 10;
		num += (str[i] - 48);
		i++;
	}
	if (neg)
		return (-num);
	else
		return (num);
}
