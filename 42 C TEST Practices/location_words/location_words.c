int		location_words(int i, char *str)
{
	int j;

	j = 0;
	while (str[j])
	{
		if ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
			i++;
		else
			return (i);
		j++;
	}
	return (i);
}
