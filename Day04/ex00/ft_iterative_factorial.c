int	ft_iterative_factorial(int nb)
{
	int		i;
	int		p;

	i = 1;
	p = 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (i <= nb)
	{
		p *= i;
		i++;
	}
	return (p);
}
