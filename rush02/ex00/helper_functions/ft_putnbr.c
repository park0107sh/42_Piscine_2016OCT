#include "rush_2.h"

void	ft_putnbr(int nb)
{
	int long nc;

	nc = nb;
	if (nc < 0)
	{
		ft_putchar('-');
		nc = (-1) * nc;
	}
	if (nc >= 10)
	{
		ft_putnbr(nc / 10);
		ft_putchar(nc % 10 + 48);
	}
	if (nc >= 0 && nc < 10)
		ft_putchar(nc + 48);
}
