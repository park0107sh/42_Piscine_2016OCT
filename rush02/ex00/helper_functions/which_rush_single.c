int		which_rush_single(char *str)
{
	int rush_no;

	if (str[0] == 'o')
		rush_no = 0;
	if (str[0] == '/')
		rush_no = 1;
	if (str[0] == 'A')
		rush_no = 234;
	if (!(str[0] == 'o' || str[0] == '/' || str[0] == 'A'))
		rush_no = 7;
	return (rush_no);
}
