#include "rush_2.h"

int		x_dim(char *str)
{
	int i;
	int x;

	i = 0;
	x = 0;
	while (str[i])
	{
		if (str[i] == '\n')
		{
			x = i;
			return (x);
		}
		i++;
	}
	return (0);
}
