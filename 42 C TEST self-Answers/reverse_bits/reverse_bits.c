unsigned char reverse_bits(unsigned char b) 
{
	   b = (b & 0xf0) >> 4 | (b & 0x0f) << 4;
	   b = (b & 0xcc) >> 2 | (b & 0x33) << 2;
	   b = (b & 0xaa) >> 1 | (b & 0x55) << 1;
	   return b;
}
