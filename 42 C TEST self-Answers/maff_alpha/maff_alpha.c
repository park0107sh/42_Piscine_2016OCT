#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	char a;

	a = 'a';
	while (a < 'z')
	{
		if ((a % 2) == 1)
			ft_putchar(a);
		else
			ft_putchar(a - 32);
		a++;
	}
	ft_putchar('\n');
}
